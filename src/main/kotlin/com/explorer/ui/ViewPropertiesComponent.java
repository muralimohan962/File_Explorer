package com.explorer.ui;

import javax.swing.*;

public class ViewPropertiesComponent {
    private JLabel nameField;
    private JLabel iconLabel;
    private JLabel sizeField;
    private JLabel typeField;
    private JPanel rootPanel;
    private JLabel locationField;

    public JLabel getNameField() {
        return nameField;
    }

    public JLabel getSizeField() {
        return sizeField;
    }

    public JLabel getTypeField() {
        return typeField;
    }

    public JPanel getRootPanel() {
        return rootPanel;
    }

    public JLabel getIconLabel() {
        return iconLabel;
    }

    public JLabel getLocationField() {
        return locationField;
    }
}
